function dx = ffct(t, x, u, p, userparam)
%#codegen
    dx = zeros(10, 1);
    dx(1) = x(6);
    dx(2) = x(7);
    dx(3) = x(8);
    dx(4) = x(9);
    dx(5) = x(10);
    dx(6) = u(1);
    dx(7) = u(2);
    dx(8) = u(3);
    dx(9) = 1/(x(2)*cos(x(5))) * (-2*x(7)*x(8)*cos(x(5))*sin(x(6)) ...
        -2*x(7)*x(9)*cos(x(5)) ...
        -2*x(8)*x(10)*x(2)*cos(x(4))*cos(x(5)) ...
        +2*x(8)*x(9)*x(10)*sin(x(5)) ...
        -x(1)*x(8)^2*cos(x(4)) ...
        +x(2)*x(8)^2*sin(x(4))*cos(x(4))*cos(x(5)) ...
        -9.81*sin(x(4)) ...
        +cos(x(4))*u(1) ...
        -x(2)*cos(x(4))*sin(x(5))*u(3));
    dx(10) = 1/x(2) * (-2*x(6)*x(8)*cos(x(5)) ...
        -2*x(7)*x(10) ...
        +2*x(7)*x(8)*sin(x(4)) ...
        +2*x(2)*x(8)*x(9)*cos(x(4))*cos(x(5))^2 ...
        +x(1)*x(8)^2*sin(x(4))*sin(x(5)) ...
        -x(2)*x(9)^2*sin(x(5))*cos(x(5)) ...
        +x(2)*x(8)^2*cos(x(4))*x(4)*sin(x(5))*cos(x(5)) ...
        -9.81*cos(x(4))*sin(x(5)) ...
        -sin(x(4))*sin(x(5))*u(1) ...
        +(x(2)*sin(x(4))-x(1)*cos(x(5)))*u(3));
end